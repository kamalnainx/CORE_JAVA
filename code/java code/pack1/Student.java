class Student
{
    private long acc_mobile;
    private String name, email;
    private float amountfee;

    public long getacc_mobile()
    {
        return acc_mobile;
    }

    public void setacc_mobile(long acc_mobile)
    {
        this.acc_mobile=acc_mobile;
    }

    public String getname()
    {
        return name;
        
    }
    public void setname(String name)
    {
        this.name=name;
        
    }

    public String getemail()
    {
        return email;
        
    }
    public void setemail(String email)
    {
        this.email=email;
        
    }
    public float getamountfee()
    {
        return amountfee;
        
    }
    public void setamountfee(float amountfee)
    {
        this.amountfee=amountfee;
        
    }
    
    
}