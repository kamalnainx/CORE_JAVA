import java.util.LinkedList;
/**
 * cj34_Linkedlist
 */
public class cj34_Linkedlist
{
    public static void main(String[] args)
    {
        LinkedList<String> ll1 = new LinkedList<String>();    
        
        ll1.add("apple");
        ll1.add("banana");
        ll1.add("cherry");
        ll1.add("Date");
        ll1.add("ek or apple");
        System.out.println(ll1);

        ll1.addFirst("first Apple");
        System.out.println("after add on first index"+ll1);

        ll1.addLast("Last Apple");
        System.out.println("after add on Last index"+ll1);

        ll1.removeFirst();
        System.out.println("after add on first element"+ll1);

        ll1.removeLast();
        System.out.println("after add on Last element"+ll1);

        
        System.out.println("print first element "+ll1.getFirst());

        System.out.println("print first element "+ll1.getLast());

    }
    
}
