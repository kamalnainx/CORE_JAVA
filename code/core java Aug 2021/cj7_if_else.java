public class cj7_if_else
{
    public static void main(String[] args)
    {
        int salary = 1200000;
        int tax;
        if(salary<500000)//salary0l-5l = tax0%
        {
            tax=0;
            System.out.println("user total salary tax is :- "+tax);
        }
        else if(salary>=500001 && salary<=1000000)//salary5l-10l =tax 0-5 0%+ 5-10 5%
        {
            
            // tax=(salary-500000)*5%;
            tax=((salary-500000)*5)/100;
            System.out.println("user salary is "+salary+" and total tax is "+tax);   
        }
        else if(salary>=1000001 && salary<=1500000)//salary10l-15l =tax 0-5 0% + 5-10 5% + 10-15 10%
        {
            tax=((salary-1000000)*10)/100;
            tax=0+50000+tax;
            System.out.println("user salary is "+salary+" and total tax is "+tax);   
        }

    }
}
