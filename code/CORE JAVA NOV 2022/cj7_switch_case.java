public class cj7_switch_case {

    public static void main(String[] args) {
        int num1=10;
        switch (num1) {
            case 1:
                System.out.println("i 2 3 4 get on the dance floor");
                break;
            case 10:
                System.out.println("10 ka dum");
                break;
        
            default:
                System.out.println("not found that number");
                break;
        }

    }
}
