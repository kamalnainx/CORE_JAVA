import java.util.ArrayList;
import java.util.Collections;


public class CJ24_arraylist
{
    public static void main(String[] agrs)
    {

        ArrayList<String> al1= new ArrayList<String>();

        System.out.println(al1);
        
        al1.add("apple");
        al1.add("banana");
        al1.add("cherry");
        al1.add("mango");
        
        System.out.println("before");
        System.out.println(al1);

        System.out.println(al1.get(0));
        System.out.println(al1.get(1));
        System.out.println(al1.get(2));
        System.out.println(al1.get(3));

        al1.set(0,"date");

        System.out.println("\nafter");
        System.out.println(al1);

        System.out.println(al1.get(0));
        System.out.println(al1.get(1));
        System.out.println(al1.get(2));
        System.out.println(al1.get(3));

        System.out.println(al1.size());

        for(int i=0;i<al1.size();i++)
        {
            System.out.println(al1.get(i));
        }

        for(String s:al1)
        {
            System.out.println(s);
        }

        Collections.sort(al1);
        
        System.out.println("\n\nafter sort array list one");
        for(String s:al1)
        {
            System.out.println(s);
        }

        al1.remove(0);

        System.out.println("\n\nafter remove zero index array list one vlaue");
        for(String s:al1)
        {
            System.out.println(s);
        }

        al1.clear();

        System.out.println("\n\nafter clear index array list one vlaue");
        System.out.println(al1);
        

        
    }
}
